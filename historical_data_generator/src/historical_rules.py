# ==========================================================
# MASTER CONTEXT ENGINE
# ==========================================================

def get_context_score(
    city: str,
    timestamp: datetime,
    is_holiday: bool = False,
    holiday_type: str = "Regular",
    event_multiplier: float = 1.0
):

    month = timestamp.month

    season_multiplier = get_season_multiplier(
        city,
        month
    )

    vacation_multiplier = get_vacation_multiplier(
        month
    )

    weekend_multiplier = get_weekend_multiplier(
        timestamp
    )

    holiday_multiplier = get_holiday_multiplier(
        is_holiday,
        holiday_type
    )

    event_multiplier = 1.0

    total_multiplier = (
    season_multiplier
    * vacation_multiplier
    * weekend_multiplier
    * holiday_multiplier
    * event_multiplier
)

    # Prevent unrealistic explosion
    total_multiplier = min(
        total_multiplier,
        2.50
    )

    return {

        "tourism_season": get_tourism_season(
            city,
            month
        ),

        "season_multiplier": season_multiplier,

        "vacation_multiplier": vacation_multiplier,

        "weekend_multiplier": weekend_multiplier,

        "holiday_multiplier": holiday_multiplier,

        "event_multiplier": event_multiplier,



        "total_multiplier": round(
            total_multiplier,
            3
        )
    }

