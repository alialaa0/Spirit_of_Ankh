# ==========================================================
# CROWD STATUS
# ==========================================================

def get_crowd_status(occupancy_rate: float) -> str:

    if occupancy_rate >= 0.90:
        return "Overcrowded"

    elif occupancy_rate >= 0.70:
        return "Busy"

    elif occupancy_rate >= 0.40:
        return "Moderate"

    return "Low"


